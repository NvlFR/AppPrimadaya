class BusinessDataSeeder extends Seeder
{
    private const TRANSACTION_NOTE = 'Generated by BusinessDataSeeder';

    private const EXPENSE_NOTE = 'Generated by BusinessDataSeeder';

    public function run(): void
    {
        $cashier = User::whereHas('role', fn ($query) => $query->where('name', 'kasir'))->first() ?? User::first();
        $services = Service::with('prices.paperSize')->get();
        $paperSizes = PaperSize::all()->keyBy('id');

        if (! $cashier) {
            $this->command->error('User belum tersedia. Jalankan UserSeeder terlebih dahulu.');

            return;
        }

        if ($services->isEmpty()) {
            $this->command->error('Layanan masih kosong. Jalankan ServiceSeeder terlebih dahulu.');

            return;
        }

        $customers = $this->prepareCustomers();

        $this->cleanupOldSeededData();

        $transactionProfiles = [
            ['count' => 18, 'status' => 'selesai', 'payment_status' => 'lunas'],
            ['count' => 8, 'status' => 'diambil', 'payment_status' => 'lunas'],
            ['count' => 9, 'status' => 'diproses', 'payment_status' => 'dp'],
            ['count' => 5, 'status' => 'selesai', 'payment_status' => 'dp'],
            ['count' => 10, 'status' => 'pending', 'payment_status' => 'belum_bayar'],
        ];

        $sequence = 1;

        foreach ($transactionProfiles as $profile) {
            for ($i = 0; $i < $profile['count']; $i++) {
                $transactionDate = $this->makeTransactionDate($sequence);
                $selectedServices = $services->random(rand(1, min(3, $services->count())));

                $this->createTransaction(
                    user: $cashier,
                    services: $selectedServices instanceof Collection ? $selectedServices : collect([$selectedServices]),
                    customer: $customers->random(),
                    paperSizes: $paperSizes,
                    status: $profile['status'],
                    paymentStatus: $profile['payment_status'],
                    transactionDate: $transactionDate,
                    sequence: $sequence,
                );

                $sequence++;
            }
        }

        $this->seedMonthlyExpenses($cashier);

        $this->command->info('BusinessDataSeeder berhasil: 50 transaksi bisnis dan pengeluaran bulan ini sudah dibuat.');
    }

    private function cleanupOldSeededData(): void
    {
        Expense::where('notes', self::EXPENSE_NOTE)->forceDelete();
        Transaction::where('notes', self::TRANSACTION_NOTE)->forceDelete();
    }

