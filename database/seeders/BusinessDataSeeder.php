    private function makeTransactionDate(int $sequence): Carbon
    {
        $baseDate = match (true) {
            $sequence <= 12 => Carbon::today(),
            $sequence <= 26 => Carbon::yesterday(),
            $sequence <= 38 => Carbon::today()->subDays(2),
            default => Carbon::today()->subDays(3),
        };

        return $baseDate
            ->copy()
            ->setTime(rand(8, 20), rand(0, 59), rand(0, 59));
    }

    private function createTransaction(
        User $user,
        Collection $services,
        Customer $customer,
        Collection $paperSizes,
        string $status,
        string $paymentStatus,
        Carbon $transactionDate,
        int $sequence
    ): void {
        $items = [];
        $subtotal = 0;

        foreach ($services as $service) {
            $item = $this->buildTransactionItem($service, $paperSizes);
            $items[] = $item;
            $subtotal += $item['subtotal'];
        }

        $discountPercent = rand(0, 100) <= 30 ? rand(5, 15) : 0;
        $discountAmount = $discountPercent > 0 ? round($subtotal * ($discountPercent / 100), 2) : 0;
        $total = max($subtotal - $discountAmount, 0);

        [$paymentMethod, $amountPaid, $dpAmount, $remainingAmount, $changeAmount] = $this->resolvePaymentData(
            paymentStatus: $paymentStatus,
            total: $total,
        );

        $transaction = Transaction::create([
            'uuid' => (string) Str::uuid(),
            'transaction_number' => sprintf(
                'TRX-%s-%04d-%s',
                $transactionDate->format('YmdHis'),
                $sequence,
                strtoupper(Str::random(3))
            ),
            'customer_id' => $customer->id,
            'user_id' => $user->id,
            'subtotal' => $subtotal,
            'discount_percent' => $discountPercent,
            'discount_amount' => $discountAmount,
            'total' => $total,
            'payment_method' => $paymentMethod,
            'amount_paid' => $amountPaid,
            'change_amount' => $changeAmount,
            'status' => $status,
            'payment_status' => $paymentStatus,
            'dp_amount' => $dpAmount,
            'remaining_amount' => $remainingAmount,
            'notes' => self::TRANSACTION_NOTE,
            'created_at' => $transactionDate,
            'updated_at' => $transactionDate,
        ]);

        foreach ($items as $item) {
            TransactionItem::create([
                ...$item,
                'transaction_id' => $transaction->id,
                'created_at' => $transactionDate,
                'updated_at' => $transactionDate,
            ]);
        }
    }

    private function buildTransactionItem(Service $service, Collection $paperSizes): array
    {
        $qty = rand(1, 12);
        $paperSizeId = null;
        $paperSizeName = null;
        $printType = 'na';
        $unitPrice = (float) ($service->base_price ?? 0);

        if ($service->has_matrix_pricing && $service->prices->isNotEmpty()) {
            $servicePrice = $service->prices->random();
            $paperSize = $servicePrice->paperSize ?? $paperSizes->get($servicePrice->paper_size_id);

            $paperSizeId = $paperSize?->id;
            $paperSizeName = $paperSize?->name;
            $printType = $servicePrice->print_type;
            $unitPrice = (float) $servicePrice->price;
            $qty = rand(5, 80);
        } elseif ($service->is_per_meter) {
            $qty = rand(1, 4);
        }

        $subtotal = $unitPrice * $qty;

        return [
            'service_id' => $service->id,
            'service_name' => $service->name,
            'paper_size_id' => $paperSizeId,
            'paper_size_name' => $paperSizeName,
            'print_type' => $printType,
            'qty' => $qty,
            'unit_price' => $unitPrice,
            'subtotal' => $subtotal,
            'item_notes' => 'Data dummy transaksi bisnis',
        ];
    }

    private function resolvePaymentData(string $paymentStatus, float $total): array
    {
        if ($paymentStatus === 'lunas') {
            $extraCash = rand(0, 1) === 1 ? rand(0, 5000) : 0;

            return [
                collect(['cash', 'transfer', 'qris'])->random(),
                $total + $extraCash,
                0,
                0,
                $extraCash,
            ];
        }

        if ($paymentStatus === 'dp') {
            $dpAmount = round($total * rand(35, 60) / 100, 2);

            return [
                collect(['cash', 'transfer', 'qris'])->random(),
                $dpAmount,
                $dpAmount,
                max($total - $dpAmount, 0),
                0,
            ];
        }

        return [
            'cash',
            0,
            0,
            $total,
            0,
        ];
    }

